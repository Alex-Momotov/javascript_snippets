let a = 2;
let b = 1;

// Simple if statement
if (a > b) {
    console.log(a);
}

// Using else logic
if (a > b) {
    console.log(a)
} else {
    console.log(b)
}

// Using else-if logic
if (a > b) {
    console.log(a)
} else if (b > a) {
    console.log(b)
} else {
    console.log(a, b);
}


